var pizzaSlices = pizzaAmount * 8;

//divide slices evenly between partygoers

var slicesServed = pizzaSlices / pizzaEaters;

//print out number of slices each person ate

console.log("Each person ate " + slicesServed + " slices of pizza at the party.");

